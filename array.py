courses = ["MIT", "CYBERSECURITY", "Datascience"]

print(courses)

# Accessing an element in array
print(courses[1])

# Looping an array
for course in courses:
    print(course)

# Adding an element in an array
courses.append("Android Development")
print(courses)

# Removing an alement from an array
courses.remove ("MIT")
print(courses)
