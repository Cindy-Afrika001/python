# Arithmetic Operators
x = 10
y = 4
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # Modulus - Returns a remainder after diving two numbers

# Comparison operators
a = 9
b = 78
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # Equal to
print(a != b) # Not equal to

# Assignment Operators
w = 7
print(w)

y = 12
y += 3
print(y)

z = 9
z -= 5
print(z)

# Logical operators
num1 = 45
num2 = 34
print(num1 < num2 and num2 < num1)
print(num1 < num2 or num2 < num1)
print(not(num1 < num2 or num2 < num1))
